}

log()       { echo -e "\n\033[1;32m[$(date +'%Y-%m-%d %H:%M:%S')] $1\033[0m"; }
error_log() { echo -e "\n\033[1;31m[ERROR] $1\033[0m"; }
warn()      { echo -e "\033[1;33m[WARNING] $1\033[0m"; }
# Returns true if --resume is active and the sentinel file or directory already exists
resume_done() { [ -n "$RESUME" ] && { [ -f "$1" ] || [ -d "$1" ]; }; }

# ERR trap: with set -e the script exits silently on any non-zero command. This
# trap fires *before* the exit so the log records exactly which line and which
# command died, instead of leaving the user with an empty "Polishing..." trail.
trap 'rc=$?; error_log "Aborted at line ${BASH_LINENO[0]} (exit ${rc}) — last command: ${BASH_COMMAND}"' ERR

# Tracks samples that completed assembly but failed optional refinement / visualization steps
failed_polish=()
failed_deconcat=()
failed_circlator=()
failed_bandage=()
failed_typing=()

# ==============================================================================
# COMMAND LINE ARGUMENT PARSING
# ==============================================================================

RESUME=""
STOP_AFTER=""
SKIP_PREPROCESSING=""
SKIP_PHAGES=""
SKIP_INTEGRONS=""
SKIP_PLASMIDS=""
SKIP_TYPING=""
SKIP_KRAKEN=""
SKIP_ABR=""
INIT_DB=""
POLISH_BATCHSIZE=""

while [[ "$#" -gt 0 ]]; do
    case $1 in
        -r|--run) RUN_NAME="$2"; shift ;;
        -b|--db-dir) DB_DIR="$2"; shift ;;
        -t|--threads) THREADS_TOTAL="$2"; shift ;;
        -l|--list) SEQ_LIST_INPUT="$2"; shift ;;
        -d|--dir) BASE_DIR="$2"; shift ;;
        -p|--phastest-dir) PHASTEST_DIR="$2"; shift ;;
        -m|--minknow-dir) MINKNOW_DIR="$2"; shift ;;
        --resume) RESUME=true ;;
        # Flags for skipping steps
        --skip-preprocessing) SKIP_PREPROCESSING=true ;;
        --skip-phages) SKIP_PHAGES="--skip-phages" ;;
        --skip-integrons) SKIP_INTEGRONS="--skip-integrons" ;;
        --skip-plasmids) SKIP_PLASMIDS="--skip-plasmids" ;;
        --skip-typing) SKIP_TYPING="--skip-typing" ;;
        --skip-kraken) SKIP_KRAKEN="--skip-kraken" ;;
        --skip-abr) SKIP_ABR="--skip-abr" ;;
        --init-db) INIT_DB="--init" ;;
        --polish-batchsize) POLISH_BATCHSIZE="$2"; shift ;;
        # QC threshold overrides
        --min-read-mb)       MIN_READ_MB="$2"; shift ;;
        --chopper-q)         CHOPPER_MIN_QUALITY="$2"; shift ;;
        --chopper-len)       CHOPPER_MIN_LENGTH="$2"; shift ;;
        --chopper-headcrop)  CHOPPER_HEADCROP="$2"; shift ;;
        --abricate-minid)    ABRICATE_MIN_ID="$2"; shift ;;
        --abricate-mincov)   ABRICATE_MIN_COV="$2"; shift ;;
        # Early-stop flags
        --just-preprocessing) STOP_AFTER="preprocessing" ;;
        --just-assembly) STOP_AFTER="assembly" ;;
        -h|--help) show_help; exit 0 ;;
        *) error_log "Unknown parameter: $1"; show_help; exit 1 ;;
    esac
    shift
done

if [ -z "$RUN_NAME" ]; then
    error_log "The -r / --run parameter is mandatory."
    show_help; exit 1
fi

# Resolve relative paths before any cd changes the working directory
[ -n "$SEQ_LIST_INPUT" ] && SEQ_LIST_INPUT="$(readlink -f "$SEQ_LIST_INPUT")"
[ -n "$BASE_DIR"       ] && BASE_DIR="$(readlink -f "$BASE_DIR")"

if [ ! -d "$DB_DIR" ]; then
    error_log "Database directory not found: $DB_DIR. Please check the -b parameter."
    exit 1
fi

# ==============================================================================
# DEPENDENCY CHECKS
# ==============================================================================
# samtools, datamash, blast, etc. are checked at runtime inside their conda envs
for dep in docker dorado; do
    command -v "$dep" >/dev/null 2>&1 || { error_log "Required tool '$dep' not found in PATH. Install it before running the pipeline."; exit 1; }
done

if ! command -v docker >/dev/null 2>&1; then
    error_log "Docker not found. Required for MOB-suite, Copla, and Phastest. Install Docker and ensure the daemon is running."
    exit 1
fi

WORKDIR="${BASE_DIR}/${RUN_NAME}"

# Databases routing
KRAKEN_DB="${DB_DIR}/Kraken"
GAMBIT_DB="${DB_DIR}/gambit"
BAKTA_DB="${DB_DIR}/bakta"
MEGARES_DB="${DB_DIR}/megares/sequences"
ISFINDER_DB="${DB_DIR}/ISfinder/ISfinder-nucl.fasta"

# Conda Initialization
if [ -f "$HOME/mambaforge/etc/profile.d/conda.sh" ]; then
    source "$HOME/mambaforge/etc/profile.d/conda.sh"
elif [ -f "$HOME/miniconda3/etc/profile.d/conda.sh" ]; then
    source "$HOME/miniconda3/etc/profile.d/conda.sh"
else
    error_log "Could not find conda.sh initialization script."
    exit 1
fi

# ==============================================================================
# DIRECTORY AND DATA PREPARATION
# ==============================================================================
mkdir -p "$WORKDIR"
LOG_FILE="${WORKDIR}/aluminion_$(date +'%Y%m%d_%H%M%S').log"
exec > >(tee -a "$LOG_FILE") 2>&1

log "Starting Aluminion for run: $RUN_NAME"
log "Log file: $LOG_FILE"
cd "$WORKDIR"

# Copy MinKNOW metadata files (small, always needed)
cp "${MINKNOW_DIR}/${RUN_NAME}/no_sample_id/"*/final_summary_*.txt . 2>/dev/null || true
cp "${MINKNOW_DIR}/${RUN_NAME}/no_sample_id/"*/report_*.json      . 2>/dev/null || true

# Ensure list_seq.tsv is in place before reading barcodes from it
if [ -n "$SEQ_LIST_INPUT" ] && [ -f "$SEQ_LIST_INPUT" ]; then
    cp "$SEQ_LIST_INPUT" "list_seq.tsv"
fi

if [ ! -f "list_seq.tsv" ]; then
    echo -e "Lab_id\tStrain\tID\tBarcode\tDNA_conc\tis_repeated" > list_seq.tsv
    error_log "Empty 'list_seq.tsv' created. Fill it out and re-run."
    exit 1
fi

# Auto-translate legacy Spanish column headers (Cultivo, Cepa, ID, Barcode, Conc, Repetida)
# to the current English schema. Earlier versions of the pipeline shipped Spanish names;
# this keeps old lab sheets working without forcing the user to edit them manually.
# Field order is preserved — only the header row is rewritten.
list_header=$(head -n 1 list_seq.tsv | tr -d '\r')
if echo "$list_header" | grep -qE '^(Cultivo|Cepa)\b'; then
    warn "list_seq.tsv uses legacy Spanish column names — auto-translating to English (Lab_id, Strain, ID, Barcode, DNA_conc, is_repeated)."
    {
        echo -e "Lab_id\tStrain\tID\tBarcode\tDNA_conc\tis_repeated"
        tail -n +2 list_seq.tsv | tr -d '\r'
    } > list_seq.tsv.translated
    mv list_seq.tsv.translated list_seq.tsv
fi

# Copy only the barcode subfolders referenced in list_seq.tsv (col 4 = Barcode).
# tr -d '\r' strips CRLF line endings that appear when list_seq.tsv is exported from
# Excel on Windows — without it, "01\r" would not match any printf %02d output.
mkdir -p fastq_pass
FASTQ_SRC=$(ls -d "${MINKNOW_DIR}/${RUN_NAME}/no_sample_id/"*/fastq_pass 2>/dev/null | head -n1)
if [ -n "$FASTQ_SRC" ]; then
    tail -n +2 list_seq.tsv \
        | tr -d '\r' \
        | awk -F'\t' '{gsub(/^[[:space:]]+|[[:space:]]+$/, "", $4); print $4}' \
        | grep -Ev '^[[:space:]]*$|^[xX]$' \
        | sort -u \
        | while read -r bc; do
            padded=$(printf "%02d" "$bc")
            src="${FASTQ_SRC}/barcode${padded}"
            if [ -d "$src" ]; then
                cp -r "$src" fastq_pass/
            else
                log "Warning: barcode${padded} not found in MinKNOW fastq_pass — skipping."
            fi
        done
else
    log "Warning: MinKNOW fastq_pass directory not found. Continuing without copying reads."
fi

mkdir -p 01_reads/QC 02_filter/QC 03_assemblies/quast 04_taxonomies/{kraken2,gtdb} 05_plasmids 08_Anotacion 09_phages 10_ices 11_integrons ../repositorio/{01_reads,03_assemblies,05_plasmids,08_Anotacion,09_phages,10_ices,11_integrons}

# ==============================================================================
# PIPELINE EXECUTION
# ==============================================================================

# 1. Read preparation and QC/filtering
if [ -z "$SKIP_PREPROCESSING" ]; then
    echo -e "ID\tR1\tR2\tLongFastQ\tFast5\tGenomeSize\tFasta" > samplesheet.tsv

    # Strip CRLF (Excel-exported TSVs) before reading fields. Without this, the last
    # field of each line keeps a trailing \r that breaks printf, file lookups, and grep.
    tail -n +2 list_seq.tsv | tr -d '\r' | while IFS=$'\t' read -r cult cep id bc c rep; do
        [ -z "$id" ] && continue
        # Trim leading/trailing whitespace from the barcode in case the TSV has padded cells.
        bc="${bc#"${bc%%[![:space:]]*}"}"
        bc="${bc%"${bc##*[![:space:]]}"}"
        # Zero-pad barcode to match MinKNOW's two-digit directory naming (barcode01, barcode09…).
        # The copy step above already pads with printf "%02d"; this keeps the two in sync.
        padded_bc=$(printf "%02d" "$bc")
        if resume_done "01_reads/${id}.fastq.gz"; then
            log "  [resume] Read concat: ${id} already done, skipping."
        elif [ -z "$rep" ]; then
            cat fastq_pass/barcode${padded_bc}/*.fastq.gz > 01_reads/${id}.fastq.gz
            cp 01_reads/${id}.fastq.gz ../repositorio/01_reads/${id}.fastq.gz
        else
            cat ../repositorio/01_reads/${id}.fastq.gz fastq_pass/barcode${padded_bc}/*.fastq.gz > 01_reads/${id}.fastq.gz
            cp 01_reads/${id}.fastq.gz ../repositorio/01_reads/${id}.fastq.gz
        fi
        echo -e "${id}\t\t\t$PWD/01_reads/${id}.fastq.gz\t\t" >> samplesheet.tsv
    done

    cut -f3 list_seq.tsv | tail -n+2 > ../repositorio/samples
    find 01_reads -type f -name "*.fastq.gz" -size "+${MIN_READ_MB}M" -exec basename {} .fastq.gz \; | sort | uniq > samples

    if [ ! -s samples ]; then
        error_log "No samples passed the ${MIN_READ_MB}MB filter."
        exit 1
    fi

    conda activate aluminion_reads

    # Chrome (used by NanoPlot's plot renderer) requires --no-sandbox on headless Linux servers.
    # Glob across the common conda installations (mambaforge / miniforge3 / miniconda3) so
    # the wrapper works regardless of which conda flavour the user installed.
    CHROME_REAL=""
    for conda_root in "$HOME/mambaforge" "$HOME/miniforge3" "$HOME/miniconda3"; do
        # `ls` returns 2 when the glob has no match; under `set -o pipefail` that
        # exit code propagates through `| head -1` and kills the script via `set -e`
        # before the loop can try the next conda root. `|| true` neutralises it.
        candidate=$(ls "$conda_root/envs/aluminion_reads/lib/python"*/site-packages/choreographer/cli/browser_exe/chrome-linux64/chrome 2>/dev/null | head -1 || true)
        if [ -n "$candidate" ]; then
            CHROME_REAL="$candidate"
            break
        fi
    done
    if [ -n "$CHROME_REAL" ] && [ -x "$CHROME_REAL" ]; then
        CHROME_WRAPPER="${WORKDIR}/.chrome_wrapper"
        printf '#!/bin/bash\nexec "%s" --no-sandbox --disable-gpu --disable-dev-shm-usage "$@"\n' "$CHROME_REAL" > "$CHROME_WRAPPER"
        chmod +x "$CHROME_WRAPPER"
        export BROWSER_PATH="$CHROME_WRAPPER"
    fi

    log "Pre-filtering QC..."
    for i in $(cat samples); do
        resume_done "01_reads/QC/${i}/NanoStats.txt" && continue
        MPLBACKEND=Agg NanoPlot --fastq 01_reads/${i}.fastq.gz -o 01_reads/QC/${i} --downsample 20000 --threads 4 --loglength &
    done

    log "Filtering (Chopper)..."
    for i in $(cat samples); do
        resume_done "02_filter/${i}.fastq.gz" && { log "  [resume] Chopper: ${i} done, skipping."; continue; }
        gunzip -c 01_reads/${i}.fastq.gz \
            | chopper -q $CHOPPER_MIN_QUALITY -l $CHOPPER_MIN_LENGTH --headcrop $CHOPPER_HEADCROP --threads $THREADS_TOTAL \
            | gzip > 02_filter/${i}.fastq.gz
    done

    log "Post-filtering QC..."
    for i in $(cat samples); do
        resume_done "02_filter/QC/${i}/NanoStats.txt" && continue
        MPLBACKEND=Agg NanoPlot --fastq 02_filter/${i}.fastq.gz -o 02_filter/QC/${i} --downsample 20000 --threads 4 --loglength &
    done
else
    log "Skipping preprocessing — using existing reads and QC from previous run."
    if [ ! -s "samples" ]; then
        error_log "'samples' file not found. Re-run without --skip-preprocessing first."
        exit 1
    fi
fi

[ "$STOP_AFTER" = "preprocessing" ] && { log "Done — stopping after preprocessing (--just-preprocessing). Filtered reads in 02_filter/."; exit 0; }

# 2. Taxonomy, assembly, polishing, and assembly QC
conda activate aluminion_assembly

if [ -z "$SKIP_KRAKEN" ]; then
    log "Taxonomy (Kraken2)..."
    # Check whether any sample still needs Kraken2 before the expensive /dev/shm copy
    kraken_needed=false
    for i in $(cat samples); do
        resume_done "04_taxonomies/kraken2/${i}.report" || { kraken_needed=true; break; }
    done
    if [ "$kraken_needed" = true ]; then
        cp ${KRAKEN_DB}/*.k2d /dev/shm/
        for i in $(cat samples); do
            resume_done "04_taxonomies/kraken2/${i}.report" && { log "  [resume] Kraken2: ${i} done, skipping."; continue; }
            kraken2 --memory-mapping --db /dev/shm --minimum-base-quality 10 --minimum-hit-groups 100 --output 04_taxonomies/kraken2/${i}.out --use-names --report 04_taxonomies/kraken2/${i}.report --gzip-compressed 02_filter/${i}.fastq.gz --threads $THREADS_TOTAL
        done
        rm -f /dev/shm/*.k2d
    else
        log "  [resume] All Kraken2 reports found — skipping database copy."
    fi
else
    log "Skipping Kraken2..."
fi

log "Assembly (Flye)..."
failed_assembly=()
for i in $(cat samples); do
    if resume_done "03_assemblies/${i}/assembly.fasta"; then
        log "  [resume] Flye: ${i} already assembled, skipping."
        continue
    fi
    if ! flye --nano-hq 02_filter/${i}.fastq.gz --threads $THREADS_TOTAL --out-dir 03_assemblies/${i}; then
        echo ""
        echo "  ┌──────────────────────────────────────────────────────────────────────┐"
        echo "  │  ASSEMBLY FAILED: ${i}                                               |"
        echo "  │  Flye could not assemble this sample (no disjointigs produced).      |"
        echo "  │                                                                      │"
        echo "  │  Options:                                                            │"
        echo "  │    1) Skip sample and continue with the rest                         │"
        echo "  │    2) Retry with --meta (for high-copy or fragmented assemblies)     │"
        echo "  │    3) Stop pipeline for manual inspection                            │"
        echo "  └──────────────────────────────────────────────────────────────────────┘"
        read -rp "  Choose [1/2/3]: " flye_choice
        case "$flye_choice" in
            1)
                error_log "Skipping sample ${i} — it will be excluded from all downstream steps."
                failed_assembly+=("$i")
                ;;
            2)
                log "Retrying Flye with --meta for sample ${i}..."
                rm -rf 03_assemblies/${i}
                if ! flye --nano-hq 02_filter/${i}.fastq.gz --threads $THREADS_TOTAL --out-dir 03_assemblies/${i} --meta; then
                    error_log "Flye --meta also failed for ${i}. Skipping sample."
                    failed_assembly+=("$i")
                else
                    log "Flye --meta succeeded for sample ${i}."
                fi
                ;;
            3)
                error_log "Pipeline stopped by user after assembly failure on sample ${i}."
                exit 1
                ;;
            *)
                error_log "Invalid choice. Stopping pipeline."
                exit 1
                ;;
        esac
    fi
done

if [ ${#failed_assembly[@]} -gt 0 ]; then
    log "Removing ${#failed_assembly[@]} failed sample(s) from pipeline: ${failed_assembly[*]}"
    for s in "${failed_assembly[@]}"; do
        sed -i "/^${s}$/d" samples
    done
fi

log "Polishing (dorado polish with @RG injection)..."
for i in $(cat samples); do
    if resume_done "03_assemblies/${i}/.polished"; then
        log "  [resume] Polishing: ${i} done, skipping."
        continue
    fi

    # dorado polish requires the basecaller model in the BAM @RG DS field.
    # Dorado embeds it in the FASTQ description — extract it before alignment.
    # `zcat | head -n 1` would trigger SIGPIPE on zcat under set -o pipefail and
    # kill the script silently. Reading via process substitution avoids that:
    # the substituted command's exit status is not part of the foreground pipeline.
    if [ ! -f "02_filter/${i}.fastq.gz" ]; then
        warn "  Missing reads file for ${i} — polishing skipped."
        failed_polish+=("$i")
        continue
    fi
    header=""
    IFS= read -r header < <(zcat 02_filter/${i}.fastq.gz) || true

    if echo "$header" | grep -q 'basecall_model_version_id='; then
        model=$(echo "$header" | awk -F 'basecall_model_version_id=' '{print $2}' | awk '{print $1}')
    else
        # Older Dorado header format without explicit basecall_model_version_id field
        model=$(echo "$header" | grep -o 'dna_[^[:space:]]*' | rev | cut -f3- -d'_' | rev)
    fi

    if [ -z "$model" ]; then
        warn "  Could not detect basecaller model for ${i} — polishing skipped."
        failed_polish+=("$i")
        continue
    fi
    log "  Basecaller model detected for ${i}: ${model}"

    # Stream aligner -> sort -> addreplacerg in a single pipe; the previous version
    # wrote the sorted BAM to disk and re-read it through addreplacerg, doubling I/O
    # per sample. samtools addreplacerg reads BAM from stdin with the `-` argument.
    if echo "$header" | grep -q 'RG:Z:'; then
        # Newer Dorado FASTQ format includes RG:Z: tag.
        # Use dorado aligner --add-fastq-rg to propagate it, then overwrite DS field (-w).
        log "  [polish] FASTQ has RG:Z: — using dorado aligner --add-fastq-rg | sort | addreplacerg"
        rg=$(echo "$header" | awk -F 'RG:Z:' '{print $2}' | awk '{print $1}')
        dorado aligner --add-fastq-rg \
                03_assemblies/${i}/assembly.fasta 02_filter/${i}.fastq.gz \
            | samtools sort -@ $THREADS_TOTAL \
            | samtools addreplacerg -w \
                -r "@RG\tID:${rg}\tDS:basecall_model=${model}" \
                -O bam -o 03_assemblies/${i}/${i}_aligned_reads.bam -
    else
        # Standard Dorado FASTQ (no RG:Z:): dorado aligner adds PN:dorado to @PG,
        # which dorado polish verifies. minimap2 would fail this check.
        log "  [polish] No RG:Z: tag — using dorado aligner | sort | addreplacerg"
        dorado aligner --threads $THREADS_TOTAL \
                03_assemblies/${i}/assembly.fasta 02_filter/${i}.fastq.gz \
            | samtools sort -@ $THREADS_TOTAL \
            | samtools addreplacerg \
                -r "@RG\tID:1\tDS:basecall_model=${model}" \
                -O bam -o 03_assemblies/${i}/${i}_aligned_reads.bam -
    fi
    samtools index -@ $THREADS_TOTAL 03_assemblies/${i}/${i}_aligned_reads.bam

    # --bacteria uses a move-table-free model; GPU auto-detected (remove flag for CPU-only servers).
    # `${POLISH_BATCHSIZE:+--batchsize $POLISH_BATCHSIZE}` expands to nothing when the user did not
    # set the flag, so dorado falls back to its own default. Override via --polish-batchsize.
    if dorado polish --threads $THREADS_TOTAL --bacteria \
            ${POLISH_BATCHSIZE:+--batchsize $POLISH_BATCHSIZE} \
            03_assemblies/${i}/${i}_aligned_reads.bam \
            03_assemblies/${i}/assembly.fasta \
            > 03_assemblies/${i}/polished_assembly.fasta; then
